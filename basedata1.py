import numpy as np
from matplotlib import pyplot as plt
import csv

X = []
target = []
for n in range(100):
    x = np.random.rand()
    y = np.random.rand()
    
    if (x-0.5)**2 + (y-0.5)**2 >.1:
        c = 1
    else:
        c = -1
    X.append([x,y])
    target.append(c)
X = np.array(X)
plt.scatter(X[:,0],X[:,1],c = target)
plt.show()

with open('dataset1.csv','w',newline = '') as f:
    w = csv.writer(f)
    for i in range(len(X)):
        w.writerow([X[i][0],X[i][1],target[i]])

